    void cargar(String valor) {

        String[] titulos = {"IDpago", "CÃ³digo", "Fecha", "Monto", "idlic02"};

        String[] registros = new String[5];

        String sql = "SELECT * FROM pago where idpc LIKE '%" + valor + "%'";

        model = new DefaultTableModel(null, titulos);

        conectar cc = new conectar();

        Connection cn = cc.conexion();
        try {

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {

                registros[0] = rs.getString("idpc");
                registros[1] = rs.getString("codigop");
                registros[2] = rs.getString("fechap");
                registros[3] = rs.getString("monto");
                registros[4] = rs.getString("idlic02");

                model.addRow(registros);
            }

            table01.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

