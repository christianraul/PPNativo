    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        String codigop, fechap, monto;
        String sql = "";
        //idpc=txtidpago.getText();
        //fechap = txtfechapag.getText();
        codigop = txtcod.getText();
        monto = txtmo.getText();
        registro.txtidlic.setText(txtlic.getText());

        sql = "INSERT INTO pago (codigop,fechap,monto,idlic02) VALUES (?,?,?,?)";
        try {
            PreparedStatement psd = cn.prepareStatement(sql);
            //psd.setString(1,idpc);
            psd.setString(1, codigop);
            //Definir variable para la fecha
            String fecha1 = null;
            java.util.Date fecha = jDateChooser1.getDate();
            SimpleDateFormat adf = new SimpleDateFormat("yyyy-MM-dd");
            fecha1=adf.format(fecha);
            psd.setString(2, fecha1);
           // psd.setString(2, fechap);
            psd.setString(3, monto);
            psd.setString(4, txtlic.getText());

            int n = psd.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Registro Guardado");
            }
        } catch (SQLException ex) {
            Logger.getLogger(pago.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        bloquer();
    }//GEN-LAST:event_btnGuardarActionPerformed

